from turtle import *
t = Turtle()
t.speed(0)
side, size = int(input()),int(input())
t.pu()
t.rt(60)
t.fd(side)
t.lt(120)
# t.goto(size,0)
# t.lt(120)
t.pd()
for _ in range(6):
    t.fd(side)
    t.lt(60)
side-=side/7
t.pu()
t.home()
t.color('red')
t.rt(60)
t.fd(side)
t.lt(120)
t.pd()
t.begin_fill()
for _ in range(6):
    t.fd(side)
    t.lt(60)
t.end_fill()

t.color('#fff')
t.pu()
t.goto(0,-size*0.5)
t.pd()
t.write('STOP',font=('Arial',size,'bold'),align='center')
t.ht()
done()
