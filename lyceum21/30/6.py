plate.fd(10)
plate.rt(90)
plate.bk(10)

plate.color('white')
plate.pensize(8)
plate.fd(130)
plate.lt(90)
plate.circle(-20, 90)
plate.lt(90)
plate.fd(90)
plate.lt(90)
plate.circle(-20, 90)
plate.lt(90)
plate.fd(540)
plate.lt(90)
