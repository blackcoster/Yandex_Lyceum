from turtle import Turtle, done

plate = Turtle()
plate.speed(0)
plate.color(input())
plate.fd(150)
plate.begin_fill()
plate.fd(150)
plate.lt(90)
plate.fd(150)
plate.lt(90)
plate.fd(600)
plate.lt(90)
