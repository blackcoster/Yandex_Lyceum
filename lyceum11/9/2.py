num1 = int(input())
num2 = int(input())
if num1 > num2:
     print(num1, 'больше чем', num2)
if num1 < num2:
     print(num1, 'меньше чем', num2)
if num1 >= num2:
     print(num1, 'больше или равно', num2)
if num1 <= num2:
     print(num1, 'меньше или равно', num2)
if num1 == num2:
     print(num1, 'равно', num2)
if num1 != num2:
     print(num1, 'не равно', num2)





